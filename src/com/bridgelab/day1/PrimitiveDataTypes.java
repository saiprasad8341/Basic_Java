package com.bridgelab.day1;

public class PrimitiveDataTypes {
    static byte b;
    static int i;
    static short st;
    static long lg;
    static float ft;
    static double db;
    static char ch;
    static boolean bool;
    public static void main(String[] args) {
        System.out.println("Default value for byte : " + b);
        System.out.println("Default value for int : " + i);
        System.out.println("Default value for short : " + st);
        System.out.println("Default value for long : " + lg);
        System.out.println("Default value for float : " + ft);
        System.out.println("Default value for double : " + db);
        System.out.println("Default value for char : " + ch);
        System.out.println("Default value for boolean : " + bool);
    }
}
